/*  Person's information
*/

public class PersonDetails {

    public String name;
    public int age;
    public String gender;

    // constructor
    public PersonDetails(String name, int age, String gender) {
        // initialises the name
        this.name = name;
        // initialises the age
        this.age = age;
        // initialises the gender
        this.gender = gender;
    }
}